    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        double farenheit = 0.0;
        int gcent = 0;
        try{
            gcent = Integer.parseInt(Centi.getText().trim());
           farenheit = (gcent * 9 / 5) + 32; //f√≥rmula para convertir grados centigrados a farenheit
           Far.setText(String.valueOf(farenheit));
           Res.setText("Resultado: " + String.valueOf(farenheit));
        }
        catch(NumberFormatException e)
        {
          JOptionPane.showMessageDialog(null, "El valor de los grados centigrados no es correcto");
          Centi.setText(" ");//Centi.setText(null)
          Centi.requestFocus();
          Far.setText("32.00");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

